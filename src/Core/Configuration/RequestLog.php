<?php

/**
 * Contains properties used to indicate whether request and response messages are to be logged.
 */
class RequestLog {

	/**
	 * Request logging location.
	 * @var string $ServiceRequestLoggingLocation
	 */
	public $ServiceRequestLoggingLocation;
	
	/**
	 * A value indicating whether to enable reqeust response logging
	 * @var bool $EnableRequestResponseLogging
	 */
	public $EnableRequestResponseLogging;

}